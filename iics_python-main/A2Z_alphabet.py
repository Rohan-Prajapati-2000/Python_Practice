task=input("Enter Your Task: ")
if task=='e' or task=='E':
    size=int(input("Enter your number you want to enter value: "))
    li2=[]
    for i in range(0,size):
        li2.append(int(input("Enter Value: ")))
    li.extend(li2)
    print(li)
elif task=='r' or task=='R':
    search=int(input("Enter Your Value Which You Want To Replace: "))
    for i in range(0,len(li)):
        if li[i]==search:
            replace=int(input("Enter Your Value For Replace: "))
            li[i]=replace
    print(li)
elif task=='d' or task=='D':
    rem=int(input("Enter Your value to delete: "))
    for i in range(0, len(li)):
        if i==rem:
            li.remove(i)
    print(li)




else:
    print("OOps!!! Please Enter A Valid Option!!!")




























